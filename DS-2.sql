SELECT FIRST_NAME||' '||LAST_NAME AS "FULL NAME",HIRE_DATE,SALARY,DEPARTMENT_ID
FROM EMPLOYEES
WHERE FIRST_NAME NOT LIKE '%M' 
AND LAST_NAME NOT LIKE '%M'
ORDER BY (DEPARTMENT_ID);
